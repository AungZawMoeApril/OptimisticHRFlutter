class PersonalInfo {
  final String prefix;
  final String email;
  final String departmentName;
  final String mobile;
  final String hiredDate;
  final String nickname;

  PersonalInfo({
    required this.prefix,
    required this.email,
    required this.departmentName,
    required this.mobile,
    required this.hiredDate,
    required this.nickname,
  });
}