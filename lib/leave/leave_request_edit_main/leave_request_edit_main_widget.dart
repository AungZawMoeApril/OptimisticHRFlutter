}

class _LeaveRequestEditMainWidgetState extends State<LeaveRequestEditMainWidget> {
  late final TextEditingController _descriptionController;
  final _formKey = GlobalKey<FormState>();

  @override
  void initState() {
    super.initState();
    _descriptionController = TextEditingController(text: widget.editLeaveRequest.message);
    
    SchedulerBinding.instance.addPostFrameCallback((_) {
      final leaveEditState = context.read<LeaveEditState>();
      leaveEditState.selectedLeaveTypeId = widget.editLeaveRequest.timeOffId;
      leaveEditState.isAllDay = widget.editLeaveRequest.wholeDay;
      leaveEditState.startDate = DateTime.parse(widget.editLeaveRequest.startDate);
      leaveEditState.endDate = DateTime.parse(widget.editLeaveRequest.endDate);
      if (widget.editLeaveRequest.startTime != null) {
        final startParts = widget.editLeaveRequest.startTime!.split(':');
        leaveEditState.startTime = TimeOfDay(
          hour: int.parse(startParts[0]),
          minute: int.parse(startParts[1]),
        );
      }
      if (widget.editLeaveRequest.endTime != null) {
        final endParts = widget.editLeaveRequest.endTime!.split(':');
        leaveEditState.endTime = TimeOfDay(
          hour: int.parse(endParts[0]),
          minute: int.parse(endParts[1]),
        );
      }
      leaveEditState.attachments = widget.editLeaveRequest.attachments;
    });
  }

  @override
  void dispose() {
    _descriptionController.dispose();
    super.dispose();
  }

